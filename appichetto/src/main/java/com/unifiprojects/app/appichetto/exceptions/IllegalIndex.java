package com.unifiprojects.app.appichetto.exceptions;

public class IllegalIndex extends IllegalArgumentException {

	private static final long serialVersionUID = 1L;

	public IllegalIndex() {
		super();
	}

	public IllegalIndex(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IllegalIndex(String arg0) {
		super(arg0);
	}

	public IllegalIndex(Throwable arg0) {
		super(arg0);
	}

}
